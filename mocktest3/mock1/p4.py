def f(fnc, prods):
    return ','.join(fnc(prod) for prod in prods)

# Example usage:
prods = ["water", "cheese", "tomato"]
fnc1 = lambda x: "id:" + x[:2]
fnc2 = lambda x: (x[0] + x[-1]).upper()

print(f(fnc1, prods))  # Output: "id:wa,id:ch,id:to"
print(f(fnc2, prods))  # Output: "WR,CE,TO"
