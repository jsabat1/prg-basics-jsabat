# Calculation of circle area and circumference 
#

# determine radius and PI values
# calculate area 
# calculate circumference 
# print results

r1=1
r2=3
pi=3.14
area1=pi*r1**2
area2=pi*r2**2
cir1=2*pi*r1
cir2=2*pi*r2
print(f'Area 1=, {area1} and circumference 1={cir1}')
print(f'Area 2=, {area2} and circumference 2={cir2}')